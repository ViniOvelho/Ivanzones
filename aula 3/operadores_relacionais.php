<h2> Operadores relacionais </h2>
<hr>
<p>
   Operadores utilizados para verificar a relação (maior, menor ou diferente) entre valores 

</p>

<strong> Operadores </strong>


<table>
<ol>

    <li> >   | maior </li>
    <li> <   | menor</li>
    <li> >=  | maior igual</li>
    <li> <=  | menor igual</li>
    <li> ==  | igual </li>
    <li> !=  | diferente </li>
    <li> === | identico - compara valor e tipo </li>
    <li> !== | não identico - compara valor e tipo </li>

</ol>

<strong> Exemplo </strong>


<pre>
 &lt;?php
 $resposta = 10 == 10;
 var_dump ($resposta);
 ?&gt;
</pre>

<?php
    $resposta = 10 > 10;
    var_dump ($resposta); 

?>

